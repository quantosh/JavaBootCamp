public class BreakAndContinue {
    public static void main(String[] args) {
        // continue skips a run, and continues to the next one.
        for (int i = 0; i<=10; i++){
            if(i % 2 != 0){
                continue;
            }
            System.out.println(i);
        }

        // break tje loop 
    }    
}
