public class ArraysAndLoops {
    public static void main(String[] args) {
        String[] kingdoms = {"Mercia", "Wesex", "Northumbria", "EA"};

        for (int i = 0; i < kingdoms.length; i++){
            System.out.println("The element at index " +i+ " is " + kingdoms[i]);
        }

        
    }
}
