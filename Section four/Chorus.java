public class Chorus {
    public static void main(String[] args) {
        singChorus();
        singChorus();       
        singChorus();
        singChorus();
    }
    
    // public - Level of access || return - Return Value | Function - Name | Parameters - (int param) | Code
    public static void singChorus(){
        System.out.println("Dont blame on the sunshine");
        System.out.println("Dont blame on the moonlight");
        System.out.println("Dont blame on the goodtimes");
        System.out.println("Blame it on the boogie\n");
    }
}
